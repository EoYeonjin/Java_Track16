package p_database;

public class Main_1_dto {
	private String id, name, area, age;

	public Main_1_dto(String id, String name, String area, String age) {
		super();
		this.id = id;
		this.name = name;
		this.area = area;
		this.age = age;
	}

	public String getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public String getArea() {
		return area;
	}

	public String getAge() {
		return age;
	}
	
}
